#include "philosophers.h"

int ft_atol(char *s)
{
    long    nb;
    int     i;
    int		sign;

    i = 0;
	sign = 1;
    nb = 0;
    while (s[i] == ' ' || (s[i] >= '\t' && s[i] <= '\r'))
        i++;
    if (s[i] == '-' || s[i] == '+')
    {
        if (s[i] == '-')
			sign *= -1;
		i++;
    }
	while (s[i] >= '0' && s[i] <= '9')
	{
		nb = nb * 10 + s[i] - '0';
		i++;
	}
	return (sign * nb);
}
